num1 = int (input ("Please enter the first number: "))    
num2 = int (input ("Please enter the second number: "))  
opt =input("enter any operator")
if opt=="+":
 print(num1 + num2)
elif opt=="-":
  print(num1-num2)
elif opt=="*":
   print(num1*num2)
elif opt=="/":
   print(num1/num2)
else:
  print("invalid option")


